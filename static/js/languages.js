// Language switching functionality
function setLanguage(lang) {
    console.log('Setting language to:', lang);
    
    const elements = document.querySelectorAll('[data-translate]');
    console.log('Found elements with data-translate:', elements.length);
    
    elements.forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            // Handle multi-line text with <br> tags
            const translatedText = translations[lang][key].replace(/\n/g, '<br>');
            
            // Check if the element contains HTML (like <br> tags)
            if (translatedText.includes('<br>') || translatedText.includes('<')) {
                el.innerHTML = translatedText;
            } else {
                el.textContent = translatedText;
            }
            
            console.log(`Translated "${key}" to "${translatedText}"`);
        } else {
            console.warn(`Missing translation for key "${key}" in language "${lang}"`);
        }
    });
    
    // Save selected language
    localStorage.setItem('selectedLanguage', lang);
    setActiveButton(lang);
}

function setActiveButton(lang) {
    document.querySelectorAll('[data-lang]').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    console.log('DOM loaded, initializing language system');
    
    // Get saved language or default to English
    const savedLang = localStorage.getItem('selectedLanguage') || 'en';
    console.log('Saved language:', savedLang);
    
    // Set initial language
    setLanguage(savedLang);
    
    // Add click handlers to language buttons
    document.querySelectorAll('[data-lang]').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            const lang = this.getAttribute('data-lang');
            console.log('Language button clicked:', lang);
            setLanguage(lang);
        });
    });
    
});